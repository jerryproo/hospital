package njxz.wangzw.model;

public class Grant {
	private int grantId;
	private String grantName;
	private String grantCode;
	private String grantAction;
	private String grantBackup;
	private String grantNo;
	private String grantIcon;
	public String getGrantIcon() {
		return grantIcon;
	}
	public void setGrantIcon(String grantIcon) {
		this.grantIcon = grantIcon;
	}
	public int getGrantId() {
		return grantId;
	}
	public void setGrantId(int grantId) {
		this.grantId = grantId;
	}
	public String getGrantName() {
		return grantName;
	}
	public void setGrantName(String grantName) {
		this.grantName = grantName;
	}
	public String getGrantCode() {
		return grantCode;
	}
	public void setGrantCode(String grantCode) {
		this.grantCode = grantCode;
	}
	public String getGrantAction() {
		return grantAction;
	}
	public void setGrantAction(String grantAction) {
		this.grantAction = grantAction;
	}
	public String getGrantBackup() {
		return grantBackup;
	}
	public void setGrantBackup(String grantBackup) {
		this.grantBackup = grantBackup;
	}
	public String getGrantNo() {
		return grantNo;
	}
	public void setGrantNo(String grantNo) {
		this.grantNo = grantNo;
	}
	
}
